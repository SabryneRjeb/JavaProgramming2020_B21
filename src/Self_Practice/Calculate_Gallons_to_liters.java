package Self_Practice;

public class Calculate_Gallons_to_liters {

    /*


     Ex:
                gallon = 10;
                output:
                    10 gallons equal to 37.8541 liters
                gallon = 20
                 output:
                    20 gallons equal to 75.7082 liters
            Hint: 1 gallon = 3.78541 liters

     */


    public static void main(String[] args) {

        double gallon = 3.78541;
        double gallontoLiters1= 10 * gallon;
        double gallontoLiters2 = 20 * gallon;

        System.out.println(gallon+" gallons equal to "+gallontoLiters1+" liters");
        System.out.println(gallon+"gallons equal to "+gallontoLiters2+"liters");





    }





}
