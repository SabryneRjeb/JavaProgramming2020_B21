package day14_Switch_Recap;

public class SwitchStatmentPractice {


    public static void main(String[] args) {

        char a = 'A';

        switch (a){

            case 'a':
                System.out.println("One");
                break;

            case 'A':
                System.out.println("Two");
                break;

        }



    }




}
